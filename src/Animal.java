public abstract class Animal {

    /**
     * Заставляет бегать животных
     * @param distance принимает расстояние в метрах, которое нужно пробежать
     */
    public abstract void run(int distance);


    /**
     * Заставляет плавать животных
     * @param distance принимает расстояние в метрах, которое нужно пробежать
     */
    public abstract void sweem(int distance);
}
